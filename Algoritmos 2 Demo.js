// Merge Sort:
// Escenario Favorable:Merge Sort es especialmente eficiente para ordenar listas enlazadas debido a su naturaleza de división y combinación.
// Funciona bien en entornos donde se necesita ordenar grandes conjuntos de datos, ya que su complejidad temporal es O(nlogn).
// Escenario Desfavorable:Puede no ser la mejor opción para conjuntos de datos pequeños o para arreglos ya parcialmente ordenados, ya que suelen haber operaciones de combinación innecesarias.
// Ventajas:Estable (mantiene el orden relativo de elementos iguales).
// Garantiza un rendimiento consistente en diversos escenarios debido a su complejidad temporal predecible.
//
//
// Quick Sort:
// Escenario Favorable:Quick Sort tiende a funcionar mejor en conjuntos de datos más pequeños o en arreglos parcialmente ordenados.
// Es eficiente en términos de espacio ya que realiza la clasificación "in-place" (sin necesidad de espacio adicional).
// Escenario Desfavorable:Puede ser menos eficiente en arreglos completamente ordenados debido a la elección del pivote, lo que lleva a divisiones desequilibradas.
// Ventajas:Tiene una buena eficiencia promedio y un rendimiento rápido en muchos casos prácticos.
// Funciona bien con conjuntos de datos grandes.
//
//
// Resumen:
// Merge Sort:Mejor rendimiento en arreglos grandes y listas enlazadas.
// Rendimiento predecible y estable.
// Quick Sort:Eficiente para conjuntos de datos más pequeños y en muchos casos prácticos.
// Funciona bien en situaciones donde el espacio adicional es limitado.
